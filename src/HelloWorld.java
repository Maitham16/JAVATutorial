// first lesson in java
// compile: javac HelloWorld.java
// run: java HelloWorld
// public: is a keyworld that means this class is available to other classes
// static: is a keyworld that means a class can be accessed without creating 
// an object of the class
// void: is a keyworld that means this method does not have a return value
// main: is the name of the method
// String[] args: means that this method accepts an array of string objects as arguments
// System.out.println(): is a function that outputs data to the console
// ;: is a statement terminator

public class HelloWorld {
    public static void main(String[] args) throws Exception {
        System.out.println("Hello, World!");
    }
}
